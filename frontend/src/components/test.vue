<script>
import navbar from "@/components/navbar.vue";
import navbar1 from "@/components/nav/navbar1.vue";
import * as d3 from "d3";
import axios from "axios";
import cloud from "d3-cloud";

export default {
  mounted() {
    this.getWords();
  },
  data() {
    return {
      words: [],
      picUrl: "",
    };
  },
  components: {
    navbar,
    navbar1,
  },
  methods: {
    async getWords() {
      return new Promise((resolve, reject) => {
        const url = "http://localhost:5000/get_zhongyuan_cloud";
        axios
          .get(url)
          .then((res) => {
            //console.log(res.data);
            const words = [];
            for (var i = 0; i < res.data.length; i++) {
              if(res.data[i].length<2){
                words.push({ text: res.data[i], txtwidth: 1 });
              };
            }
            console.log(words);
            this.words = words;

            const svg = d3
              .select(this.$refs.svg)
              .attr("width", 700)
              .attr("height", 800);

            const layout = cloud()
              .size([500, 300])
              .words(words)
              .padding(5)
              .rotate(() => 0) // 设置旋转角度为0，即不旋转
              .font("Impact")
              .fontSize(30)
              .on("end", this.draw);

            layout.start();
          })
          .catch(function (err) {
            reject(err);
          });
      });
    },

    draw(words) {
      const svg = d3.select(this.$refs.svg);

      svg.selectAll("g").remove();

      const g = svg.append("g").attr("transform", "translate(350,350)");

      g.selectAll("rect")
        .data(words)
        .enter()
        .append("rect")
        .on("click", (event, d) => this.wordClicked(d.text))
        .attr("x", (d) => d.x - d.txtwidth * 15 - 10)
        .attr("y", (d) => d.y - 20)
        .attr("width", (d) => d.txtwidth * 30 + 20)
        .attr("height", (d) => 40)
        .style("fill", "red")
        .attr("rx", 8)
        .attr("ry", 8)
        .style("opacity", 0.3);

      g.selectAll("text")
        .data(words)
        .enter()
        .append("text")
        .on("click", (event, d) => this.wordClicked(d.text))
        .attr("x", (d) => d.x)
        .attr("y", (d) => d.y)
        .style("font-size", (d) => 30 + "px")
        .style("font-family", "Impact")
        .style("fill", "white")
        .attr("text-anchor", "middle")
        .attr("alignment-baseline", "middle")
        .text((d) => d.text);
    },

    async wordClicked(word) {
      // Send the clicked word to the backend using an API call
      try {
        console.log(word);
        const response = await axios.post(
          "http://localhost:5000/get_zhongyuan_url",
          {
            params: {
              Word: word,
            },
          }
        );
        console.log("Word clicked:", word);
        console.log("Backend response:", response.data);
        console.log("Backend response url:", response.data['data']);
        this.picUrl = response.data['data'];
      } catch (error) {
        console.error("Error sending clicked word to the backend:", error);
      }
    },
  },
};
</script>
        
